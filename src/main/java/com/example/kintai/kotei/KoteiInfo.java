package com.example.kintai.kotei;

public class KoteiInfo {
	private String koteicode;

	private String Koteimeisyo;

	private String Koteiteigi;

	public String getKoteicode() {
		return koteicode;
	}

	public void setKoteicode(String koteiCode) {
		this.koteicode = koteiCode;
	}

	public String getKoteimeisyo() {
		return Koteimeisyo;
	}

	public void setKoteimeisyo(String koteimeisyo) {
		Koteimeisyo = koteimeisyo;
	}

	public String getKoteiteigi() {
		return Koteiteigi;
	}

	public void setKoteiteigi(String koteiteigi) {
		Koteiteigi = koteiteigi;
	}

}
